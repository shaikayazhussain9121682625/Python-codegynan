a = 10
b = 20

# Addition
c = a + b
print('addition of two numbers', c)

# Subtraction
c = a - b
print('subtraction of two numbers', c)

# Multiplication
c = a * b
print('multiplication of two numbers', c)

# Division
c = a / b
print('division of two numbers', c)

# Floor Division
c = a // b
print('floor division of two numbers', c)

# Modulus
c = a % b
print('modulus of two numbers', c)

# Exponent
c = a ** b
print('exponent of two numbers', c)