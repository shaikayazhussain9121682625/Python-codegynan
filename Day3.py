#Assignment Operators
a = 10

print("Initial value:", a)

a += 5      # a = 10 + 5 = 15
print("After += :", a)

a -= 3      # 15 - 3 = 12
print("After -= :", a)

a *= 2      # 12 * 2 = 24
print("After *= :", a)

a /= 4      # 24 / 4 = 6.0
print("After /= :", a)

a %= 4      # 6 % 4 = 2
print("After %= :", a)

a //= 2     # 2 // 2 = 1
print("After //= :", a)

a **= 3     # 1 ** 3 = 1
print("After **= :", a)